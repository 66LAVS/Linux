#!/bin/bash

# Чтение двух целых чисел A и B
read -p "Введите первое целое число A: " A
read -p "Введите второе целое число B (A < B): " B

# Проверка условия A < B
if (( A >= B )); then
echo "Ошибка: A должно быть меньше B."
exit 1
fi

# Вывод чисел от A до B и подсчет количества
count=0
for (( i=A; i<=B; i++ )); do
echo "$i"
((count++))
done

# Вывод количества чисел
echo "Количество чисел между A и B (включая A и B): $count"
